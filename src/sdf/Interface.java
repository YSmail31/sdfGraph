    private void ouvrir() {
        jf=new JFileChooser();
        jf.setCurrentDirectory(new File("."));
        int res=jf.showOpenDialog(this);
        if(res == JFileChooser.APPROVE_OPTION)
        {
            example = new Graphe(jf.getSelectedFile().getAbsolutePath());
            jScrollPane1.setViewportView(example);
        }
    }
